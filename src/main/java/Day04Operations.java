public class Day04Operations {
    public static void main(String[] args) {
        //Logical Operator

        System.out.println(5<4 || 10>9); //true, so it returns boolean type

        // Post and Pre increment

        //Post increment
        int x = 4;
        int y = x++;
        System.out.println(y);

        //Pre increment
        int z = 4;
        int q= ++z;
        System.out.println(q);
    }
}
