public class Day11DoWhileLoop01 {
    public static void main(String[] args) {
        //Compare the given loops

        int i= 1;

        while (i < 1){
            System.out.println("While");
            i++;
        }


        int k =1;

        do{
            System.out.println("Do-while");
            k++;
        }while(k<1);

    }
}
